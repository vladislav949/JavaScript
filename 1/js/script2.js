/* Цикл for ------------------------
В цикле for все условия записываются в одном месте, что очень удобно
во многих случаях.
*/
// Первоначальное значение не может бытьстрокой или булевым значением.
for(var i = 0; i <= 10; i++) {
  console.log("i: " + i);
}
/* Цикл будет выполняться 10 раз, с изначальным значением 0, за каждую итерацию
цикла значение будет увеличиваться на еденицу и так будет до тех пор пока i
будет меньше или равно числу 10 */

/* Цикл while -----------------------
Суть цикла while не особо отличается от цикла for. Единственное отличие
заключается в способе записи цикла. В while необходимо прописать лишь условие,
а все остальные параметры записываются вне цикла:
*/
var j = 0;
while(j < 10) { // Условие
  console.log("j :" + j);
  j++; // увеличение переменной
} /* Пока j будет меньше чем 10, цикл будет выполняться */

